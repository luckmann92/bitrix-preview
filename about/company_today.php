<h2>Цели устойчивого развития ООН</h2>
<p>
	 В сентябре 2015 года на&nbsp;70-й сессии Генеральной ассамблеи ООН принята новая мировая повестка&nbsp;— «Преобразование нашего мира: Повестка дня в&nbsp;области устойчивого развития на&nbsp;период до 2030 года», включающая 17 Целей устойчивого развития (ЦУР), которые пришли на&nbsp;смену Целям развития тысячелетия.
</p>
<p>
	 Одна из особенностей вновь принятых целей заключается в том, что они адресованы не только правительствам, но и другим участникам процесса устойчивого развития в мире: бизнесу, гражданскому обществу, всем частным лицам.
</p>
<p>
	 В Холдинге проводится работа над изучением ЦУР и определением вклада Холдинга в их достижение, включая:
</p>
<ol>
	<li>
	<p>
		 соотнесение задач и индикаторов ЦУР с целями, задачами, приоритетами и направлениями деятельности Холдинга;
	</p>
 </li>
	<li>
	<p>
		 принятие обязательств в отношении ЦУР;
	</p>
 </li>
</ol>
<p>
	 При формировании проектного портфеля Холдинг отдает предпочтение проектам, которые соответствуют ЦУР и оказывают значительное влияние на устойчивое социально-экономическое развитие Республики Казахстан. В частности, проекты должны способствовать:
</p>
<ul>
	<li>
	<p>
		 всеохватной и устойчивой индустриализации;
	</p>
 </li>
	<li>
	<p>
		 открытости, безопасности, жизнестойкости и экологической устойчивости городов и населенных пунктов;
	</p>
 </li>
	<li>
	<p>
		 диверсификации экономики Республики Казахстан и отдельных областей/ городов.
	</p>
 </li>
</ul>
<ol>
	<li>
	<p>
		 интеграцию обязательств и целей с процессами и практиками Холдинга;
	</p>
 </li>
</ol>
<p>
	 Существующие программы, реализуемые Холдингом, содействуют достижению большинства ЦУР и их задачам. В то же время с целью соответствия конкретным задачам и индикаторам ЦУР Холдингом будет продолжена работа в отношении интеграции ЦУР в процессы и практики Холдинга.
</p>
<ol>
	<li>
	<p>
		 раскрытие информации о содействии в достижении ЦУР.
	</p>
 </li>
</ol>
<p>
	 В годовом отчете за 2017 год Холдингом впервые раскрывается информация о приверженности руководства и наиболее приоритетных для Холдинга ЦУР.
</p>
