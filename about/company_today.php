<h2>Кадровая политика</h2>
<p>
	 Корпоративная культура
</p>
<p>
	 Работники ‑ главная ценность и основной ресурс, от уровня их профессионализма и безопасности напрямую зависят результаты деятельности Холдинга.
</p>
<p>
	 С целью поддержания корпоративной культуры, основанной на высоких этических стандартах, все работники Холдинга руководствуются общими принципами деловой этики, такими как доверие, взаимное уважение и порядочность.
</p>
<p>
	 Развитие корпоративной культуры основывается на достижении следующих условий:
</p>
<ul>
	<li>
	<p>
		 руководители создают климат, который мотивирует сотрудников прикладывать максимальные усилия для достижения результатов;
	</p>
 </li>
	<li>
	<p>
		 поощряются инициатива снизу и инновации;
	</p>
 </li>
	<li>
	<p>
		 самосовершенствование и саморазвитие являются осознанной необходимостью;
	</p>
 </li>
	<li>
	<p>
		 должностное лицо и работник фокусируются на конечном результате и успехе общего дела;
	</p>
 </li>
	<li>
	<p>
		 каждое должностное лицо и работник понимает свою роль и значение в достижении стратегических целей;
	</p>
 </li>
	<li>
	<p>
		 каждое решение принимается с учетом социальной направленности бизнеса и ориентации на основных заинтересованных лиц Холдинга.
	</p>
 </li>
</ul>
<p>
	 Неотъемлемой частью корпоративной культуры Холдинга является проведение мероприятий, направленных на сплочение командного духа, в которых принимают участие работники Холдинга и его дочерних организаций.
</p>
<p>
	 В 2017 году интегрированный показатель вовлеченности персонала Группы Холдинга составил 78%.
</p>
<p>
</p>
<p>
	 Обучение и развитие
</p>
<p>
	 В Холдинге функционирует система постоянного обучения и профессионального развития работников с применением современных технологий и систем управления информацией. При организации процесса обучения используются такие формы обучения как бизнес-завтраки с экспертами, обучающие стажировки, коучинг, кейсы, мозговые штурмы, проекты и пр.
</p>
<p>
	 По итогам 2017 года достигнут полный охват обучением сотрудников Холдинга за счет проведения обучающих мероприятий корпоративного формата. Всего в 2017 году проведено 114 мероприятий по программам обучения и профессионального развития работников Холдинга в различных областях: экономика и финансы, управление проектами, право и юриспруденция, корпоративное управление, бюджетирование и финансирование, языковые курсы и пр.
</p>
<p>
</p>
<p>
	 Оценка эффективности деятельности работников
</p>
<p>
	 В Холдинге ежеквартально осуществляется оценка результативности деятельности работников на основе ключевых показателей деятельности (КПД). КПД устанавливаются в количественных и качественных значениях и отражают стратегические приоритеты Холдинга. Цикл планирования и оценки результативности работников состоит из следующих этапов:
</p>
<ul>
	<li>
	<p>
		 планирование ‑ постановка целевых КПД и составление индивидуального плана развития для достижения поставленных КПД;
	</p>
 </li>
	<li>
	<p>
		 утверждение ‑ утверждение карт КПД Правлением Холдинга и карт КПД руководства Советом директором;
	</p>
 </li>
	<li>
	<p>
		 оценка ‑ расчет и утверждение фактической результативности, подготовка отчета об исполнении плана обучения и профессионального развития.
	</p>
 </li>
</ul>
<p>
</p>
<p>
	 Безопасность и охрана труда
</p>
<p>
	 В Холдинге предпринимаются меры по обеспечению правильной организации труда работников на рабочих местах и создаются безопасные условия труда, соответствующие требованиям законодательства Республики Казахстан в этой области. С целью соблюдения правил охраны и безопасности труда в Холдинге на ежегодной основе проводятся обучающие мероприятия. Так, в 2017 году были неоднократно проведены вводные и плановые инструктажи, а также тренировки по действиям в чрезвычайных ситуациях.
</p>
<p>
</p>
<p>
	 Социальная поддержка работников
</p>
<p>
	 Холдинг обеспечивает своих работников основными социальными гарантиями и компенсациями, предусмотренными действующим законодательством Республики Казахстан, а также последовательно осуществляет и развивает дополнительные социальные программы и льготы. Так, в рамках программы социальной поддержки работников Холдинг на ежегодной основе осуществляет медицинское страхование сотрудников.
</p>
