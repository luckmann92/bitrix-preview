<h2>Подход Холдинга к устойчивому развитию</h2>
<p>
	 Холдинг «Байтерек», как ключевой финансовый институт развития Правительства Республики Казахстан, осознает важность своего влияния на экономику, экологию и общество для обеспечения устойчивого развития в долгосрочном периоде и стремится соблюдать баланс интересов заинтересованных сторон.
</p>
<p>
	 Учитывая высокую экономическую и социальную значимость, Холдинг нацелен на повышение уровня прозрачности своей деятельности перед широким кругом заинтересованных сторон посредством построения эффективной системы управления устойчивым развитием.
</p>
<p>
	 Устойчивое развитие в Холдинге состоит из трех составляющих: экономической, экологической и социальной.
</p>
