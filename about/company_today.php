<h2>Ответственное инвестирование</h2>
<p>
	 При достижении цели экономически эффективной и устойчивой деятельности Холдинг следует передовой международной практике в экологических и социальных вопросах, а также в вопросах корпоративного управления (факторы ESG) в рамках проектов, получающих финансовую поддержку Холдинга, с целью развития практики ответственного инвестирования в соответствии с основными международными стандартами в этой области ‑ Принципами ответственного инвестирования ООН и Принципами Экватора.
</p>
<p>
	 Холдинг стремится внедрять и интегрировать факторы ESG в процесс принятия инвестиционных решений, в том числе с помощью ознакомления с факторами ESG контрагентов Холдинга.
</p>
<p>
	 Холдинг ожидает от своих контрагентов:
</p>
<p>
</p>
<ul>
	<li>
	<p>
		 применения системы управления экологическими рисками (окружающей средой) по выявлению, оценке фактических и возможных воздействий на окружающую среду и общество, определения и результативного внедрения соответствующих мер управления, осуществления контроля и уровнем экологических рисков;
	</p>
 </li>
	<li>
	<p>
		 применения системы управления человеческими ресурсами и обеспечения (сохранения) здоровья работников, которая должна базироваться на уважении прав работников, включая свободу объединений и право на заключение коллективных договоров; данная система должна обеспечивать справедливое отношение к работникам, создание для них безопасных и здоровых условий труда, осуществления предупреждения и предотвращения неблагоприятного воздействия на здоровье и безопасность групп населения и потребителей;
	</p>
 </li>
	<li>
	<p>
		 применения системы добросовестного корпоративного управления, основанной на принципах справедливости, честности, ответственности, прозрачности, профессионализма и компетентности; добросовестное корпоративное управление предполагает уважение прав и интересов всех заинтересованных в деятельности организации лиц и способствует ее успешной деятельности, в том числе росту ее рыночной стоимости, поддержанию финансовой стабильности и прибыльности.
	</p>
 </li>
</ul>
