<h2>Спонсорская и благотворительная деятельность</h2>
<p>
	 Спонсорская и благотворительная деятельность Холдинга в 2017 году была направлена на оказание помощи незащищенным слоям населения и малообеспеченным гражданам, возрождению национальных ценностей, поддержке культуры, науки и образования.
</p>
<p>
	 Среди направлений благотворительной деятельности Холдинга в 2017 году ‑ оказание помощи Общественному объединению инвалидов «Центр независимая жизнь «ДОС» для организации выставки «Лучшие социальные проекты, реализуемые в Казахстане». Целью проведения данного мероприятия послужило обращение внимания гражданского общества на проблемы лиц, являющимися инвалидами. Социальные проекты, представляемые на конкурс, продемонстрировали неординарное видение существующих проблем, возможности и направления их решений.
</p>
